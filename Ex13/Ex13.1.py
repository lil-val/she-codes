from tkinter import *
root = Tk()
root.title('Hello tkinter')  # adding title to the window
root.geometry('500x500+50+300')  # geometry set the size and position of the opened window
# 500x500 defines the size of the window, +50+300 define the location of the window within the screen
# width x height + offset from left + offset from top
root.mainloop()
